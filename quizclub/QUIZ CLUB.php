"Bell MT","serif"'>What
qualities should the club members have?<o:p></o:p></span></b></p>

<p class=MsoNormal style='text-indent:1cm'><span style='font-size:14.0pt;
mso-bidi-font-size:11.0pt;line-height:115%;font-family:"Bell MT","serif"'>The club members should have good
technical and practical skills as well as general awareness which is essential
for question preparation and aptitude/intellectual skills for asking normal
questions in an innovative/different way. He/she should be honest and sincere
enough for the question paper to be kept confidential till the last minute. As
an organizer he/she should be passionate and responsible enough for the
happenings in the club. As an organizer he/she should be enthusiastic to learn
from experiences.<o:p></o:p></span></p>

<p class=MsoNormal style='text-indent:0cm'><b style='mso-bidi-font-weight:normal'><span
