So all things goes in usual way like earing good communication skills, getting
new friends, working with new people, acquainting with authority etc. And more
than that he is responsible for question paper preparation. As many think,
question paper preparation is not that much easy and it has got something to do
with their technical/general/practical knowledge and something with their
aptitude and intellectuality. We are discussing here about the rounds to be
conducted, number of questions and their toughness, syllabus to be focused, participant's
perspective, scheduling quizzes so that they do not interfere with regular
examinations and how to enhance the participant benefits (as discussed above)
etc. The ultimate goal of each and every club member is to make the participant
feel the pleasure of participating in a wonderful and healthy quiz.<o:p></o:p></span></p>

