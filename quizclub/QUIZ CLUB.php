<p class=MsoNormal style='text-indent:0cm'><b style='mso-bidi-font-weight:normal'><span
style='font-size:14.0pt;mso-bidi-font-size:11.0pt;line-height:115%;font-family:
"Bell MT","serif"'>Why Quiz and what is this Quiz Club?<o:p></o:p></span></b></p>

<p class=MsoNormal><span style='font-size:14.0pt;mso-bidi-font-size:11.0pt;
line-height:115%;font-family:"Bell MT","serif"'>Exam itself cannot measure a
person's technical and analytical skills. Having a healthy competition is
essential part in ecology as well as education. Quiz, unlike academic tests, is
not focused on a particular area, it is a test with a large domain but less
depth. To check whether the learning strategies are in a fruitful way, to check
whether you can apply your knowledge efficiently and accurately various
milestones are needed. This is the club which tries to form those milestones in
terms of your curricular/co-curricular skills. <o:p></o:p></span></p>

<p class=MsoNormal><span style='font-size:14.0pt;mso-bidi-font-size:11.0pt;
line-height:115%;font-family:"Bell MT","serif"'>It is a wing of SDCAC to
organize and prepare questions for quiz competitions (both technical and
general knowledge) in our university. Quiz club is formed by association of
students from all six branches of E3. The question paper preparation is completely
independent of other clubs of SDCAC so that organizers from other clubs can
participate in these quiz competitions. Currently 2009 batch is holding the club. </span><span style='font-family:
"Bell MT","serif"'><o:p></o:p></span></p>

<p class=MsoNormal><span style='font-size:14.0pt;mso-bidi-font-size:11.0pt;
line-height:115%;font-family:"Bell MT","serif"'><o:p>&nbsp;</o:p></span></p>

<p class=MsoNormal style='text-indent:0cm'><b style='mso-bidi-font-weight:normal'><span
style='font-size:14.0pt;mso-bidi-font-size:11.0pt;line-height:115%;font-family:
"Bell MT","serif"'>What benefits
the participants may get?<o:p></o:p></span></b></p>

<p class=MsoNormal style='text-indent:1cm'><span style='font-size:14.0pt;
mso-bidi-font-size:11.0pt;line-height:115%;font-family:"Bell MT","serif"'>To be a participant he/she should have
zeal to compete with others and to prove himself/herself among others. With
this enthusiasm he/she can earn not only name/fame but something more. To work
as a team, to distribute work (here questions), to manage time, to have a good
coordination, to have a good team spirit, decision making (for example, to
answer a question under negative marking, for which you do not know the answer
exactly) are the main skills the participants are supposed to earn, because
most of these skills may not be earned by writing a normal exam on your own..
Above all, they have to observe how the questions are asked and what is
actually expected from them and then of course, they have to assess themselves.
They can actually learn new things and find new ways of thinking for an already
paved way of thought. Each round is a milestone for them. Because out of 8000
students in the campus their names are published online for they have cleared
first round or they have stood I/II in the competition.<b style='mso-bidi-font-weight:
normal'> </b>So your achievements can catch the eye of other students and as a
result increases your confidence levels.<o:p></o:p></span></p>

<p class=MsoNormal style='text-indent:1cm'><span style='font-size:14.0pt;
mso-bidi-font-size:11.0pt;line-height:115%;font-family:"Bell MT","serif"'>You
(participant) are requested to mind the above things before participating in
the quiz. <o:p></o:p></span></p>

