<h2><span style='font-size:18.0pt;mso-bidi-font-size:13.0pt;line-height:115%;
font-family:"Bell MT","serif";margin-left:-50px;'>Science quiz:<o:p></o:p></span></h2>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal><b style='mso-bidi-font-weight:normal'><span
style='font-size:14.0pt;mso-bidi-font-size:11.0pt;line-height:115%;font-family:
"Bell MT","serif"'>How is it organized?<o:p></o:p></span></b></p>

<p class=MsoNormal><span style='font-size:14.0pt;mso-bidi-font-size:11.0pt;
line-height:115%;font-family:"Bell MT","serif"'>A science quiz is conducted in
the view of science day (28<sup>th</sup> February) for PUC students on 26<sup>th</sup>
and 27<sup>th</sup> February. Participants are asked to form groups of 3
members each. For this quiz we mainly focused on the concepts up to PUC
Semester 1 (with 90% questions from SSC level). <o:p></o:p></span></p>

<p class=MsoNormal><span style='font-size:14.0pt;mso-bidi-font-size:11.0pt;
line-height:115%;font-family:"Bell MT","serif"'>Totally 182 teams have enrolled
out of which 128 teams have appeared for quiz on 26<sup>th</sup> February. The first round consists of 30 questions of
objective type with negative marking. Duration of exam is 30 minutes. Questions
are covered from general science (physics, chemistry and biology). The marks
are announced to the participants on the spot and results are communicated
through SDCAC site. 7 teams are taken to the second round (actually there was a
tie for sixth team both in total marks and negative marks).<o:p></o:p></span></p>

<p class=MsoNormal><span style='font-size:14.0pt;mso-bidi-font-size:11.0pt;
line-height:115%;font-family:"Bell MT","serif"'>Second round is conducted on 27<sup>th</sup>
February with the grateful presence of Mr G V Rama sir. There are 3 sub-rounds.
One is regarding various instruments used for measurement. One is recognition
of scientists shown on the projector directly or by a clue (his/her invention).
The other round is a visual round, in which a video clip is played and the team
should answer the question asked related to the video. The teams have given a
wonderful performance. <o:p></o:p></span></p>

<p class=MsoNormal><span style='font-size:14.0pt;mso-bidi-font-size:11.0pt;
line-height:115%;font-family:"Bell MT","serif"'>We have organized the event
very well with good coordination which was actually lack in our previous
technical quiz. Thanks for all who made it a grand success. The winners are awarded
with the great Vivekananda books on the science day celebrations.<o:p></o:p></span></p>

