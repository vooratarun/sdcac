<h2><span style='font-size:18.0pt;mso-bidi-font-size:13.0pt;line-height:115%;
font-family:"Bell MT","serif";margin-left:-50px;'>Technical quiz:<o:p></o:p></span></h2>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal><b style='mso-bidi-font-weight:normal'><span
style='font-size:14.0pt;mso-bidi-font-size:11.0pt;line-height:115%;font-family:
"Bell MT","serif"'>How it is organized?<o:p></o:p></span></b></p>

<p class=MsoNormal><span style='font-size:14.0pt;mso-bidi-font-size:11.0pt;
line-height:115%;font-family:"Bell MT","serif"'>Technical quiz is organized by
Quiz club on 10<sup>th</sup> and 11<sup>th</sup> February 2014 for engineering
students.The question paper is prepared
separately for juniors (E1 and E2) and seniors (E3 and E4). Participants are
asked to form teams of six members with one member from each branch (for E1,
with members from both sections). Syllabus is up to E3 Sem2 for seniors quiz
and up to E2 sem1 for juniors quiz (with most of the questions from E1).<o:p></o:p></span></p>

<p class=MsoNormal><span style='font-size:14.0pt;mso-bidi-font-size:11.0pt;
line-height:115%;font-family:"Bell MT","serif"'>Totally 78 teams from seniors
and 70 teams from juniors have enrolled for this quiz. First round is conducted
on 10<sup>th</sup> February, 2014 for both juniors and seniors. Totally 60
questions are given in senior paper, for which there is negative marking. Totally
45 questions are given in junior paper, for which there is negative marking.
Duration of the quiz is 45min and 30min for senior and junior paper
respectively..Results are given on the
same day. 6 and 7 teams are selected from seniors and juniors quiz respectively
for second round.<o:p></o:p></span></p>

<p class=MsoNormal><span style='font-size:14.0pt;mso-bidi-font-size:11.0pt;
line-height:115%;font-family:"Bell MT","serif"'>Second round is conducted on
the consecutive day. It consists of three sub rounds. One related to
application oriented questions. One related to questions regarding various
equipment used in laboratories and the other is a rapid round with one minute
duration, in which one word answer questions related to all branches are asked.<o:p></o:p></span></p>

<p class=MsoNormal><span style='font-size:14.0pt;mso-bidi-font-size:11.0pt;
line-height:115%;font-family:"Bell MT","serif"'>The tie which is occurred in
the seniors quiz is broken by a small memory test.<o:p></o:p></span></p>

<p class=MsoNormal><b style='mso-bidi-font-weight:normal'><span
style='font-size:14.0pt;mso-bidi-font-size:11.0pt;line-height:115%;font-family:
"Bell MT","serif"'>A small feedback to participants from our side: <o:p></o:p></span></b></p>

<p class=MsoNormal><span style='font-size:14.0pt;mso-bidi-font-size:11.0pt;
line-height:115%;font-family:"Bell MT","serif"'>In our academic examinations,
there is no negative marking. But it is there in almost all national/state
level examinations. Until we face an exam like that, we cant know how typical
it is to make a decision while encountering a question for which you do not
know the answer exactly. At that time, you should think about the importance of
each mark in deciding your rank (it depends on the competition), how probable
is the answer to be correct against other options etc. This is the reason for
putting negative marking in the quiz. Most of the people have blindly answered
the questions so that many of them got negative aggregates. Please mind it for
future tests...!!!<o:p></o:p></span></p>

